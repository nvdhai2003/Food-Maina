package nvdhai2003.mobileapp.foodmaina.ui.models;

public class Items {
    private String title;
    private int image;

    public Items(String title, int image) {
        this.title = title;
        this.image = image;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}
